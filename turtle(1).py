	
import turtle as t
	
 
	
t.shape( "turtle" )
	
 
	
t.fillcolor( "yellow" )
	
t.begin_fill()
	
t.pencolor( "red" )
	
t.circle( 60, 360, 5 )
	
t.end_fill()
	
 
	
t.forward( 130 )
	
t.fillcolor( "gray" )
	
t.begin_fill()
	
t.pencolor( "blue" )
	
t.circle( 60, 360, 6 )
	
t.end_fill()
	
 
	
t.forward( 130 )
	
t.fillcolor( "pink" )
	
t.begin_fill()
	
t.pencolor( "yellow" )
	
t.circle( 60, 360, 7 )
	
t.end_fill()
	
 
	
t.done()